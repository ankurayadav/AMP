DATA SEGMENT
	MY_LIST DB 40H,21H,69H,1AH,55H,65H
	MIN DB ?
	MAX DB ?
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE

START:	MOV AX, DATA
		MOV DS, AX
		
		LEA BX, MY_LIST
		MOV DL,[BX]
		MOV DH,[BX]
		
		MOV SI, 01H
		
		MOV CX, 05H
L:		MOV AL, [BX+SI]
		CMP AL, DL
		JNC MX
		MOV DL, AL
		JMP N
	MX:	CMP AL, DH
		JC N
		MOV DH, AL
	N:	INC SI
		LOOP L
		
		LEA BX, MIN
		MOV [BX],DL
		LEA BX, MAX
		MOV [BX],DH
		
		MOV AH, 4CH
		INT 21H
CODE ENDS
	END START