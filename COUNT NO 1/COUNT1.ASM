DATA SEGMENT
	NUM DW 0ABCDH
	COUNT DB 00H
DATA ENDS

STK SEGMENT STACK
	DB 100 DUP(00)
	TOS LABEL WORD
STK ENDS

CODE SEGMENT
	ASSUME CS: CODE, DS: DATA, SS: STK

COUNT1 PROC
	L:	ROL NUM, 01H
		JNC X
		INC COUNT
	X:	LOOP L
		RET
COUNT1 ENDP

START:	MOV AX, DATA
		MOV DS, AX
		MOV AX, STK
		MOV SS, AX

		MOV CX, 10H
	
		CALL COUNT1
		
		MOV AH, 4CH
		INT 21H
CODE ENDS
	END START