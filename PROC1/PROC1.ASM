DATA SEGMENT
	MY_LIST DB 40H,21H,69H,1AH,55H,6AH
	ASIZE EQU $ - MY_LIST
	AVERAGE DW ?
DATA ENDS

STK SEGMENT
	ARRAY DB 100 DUP(?)
	SSIZE EQU $
STK ENDS

CODE SEGMENT
	ASSUME DS: DATA, CS:CODE, SS:STK
PROCEDURE PROC
	MOV AX,0000H
L:	ADD AL, [BX]
	JNC SKIP
	INC AH
SKIP:	INC BX
	LOOP L
	
	RET
PROCEDURE ENDP

MAIN:	MOV AX, DATA
		MOV DS, AX 
		MOV AX, STK
		MOV SS, AX
		
		MOV CX, ASIZE
		PUSH CX
		LEA BX, MY_LIST
		CALL PROCEDURE
		POP CX
		DIV CX
		
		LEA BX, AVERAGE
		MOV [BX+1], AX
		
		
		MOV AX, DX
		MOV DL, 0AH
		MUL DL
		DIV CL
		MOV [BX], AL
		
		MOV AL, AH
		MUL DL
		DIV CL
		MOV [BX], AL
		
		MOV AH, 4CH
		INT 21H
CODE ENDS
	END MAIN