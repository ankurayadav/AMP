DATA SEGMENT
	STR1 DB "Enter password :"
	LENSTR1 EQU $-STR1
	STR2 DB "ANKUR"
	LENSTR2 EQU $-STR2
	STR3 DB 100 DUP(0)
	LENSTR3 EQU $-STR3
	STR4 DB "WORNG PASSWORD           "
	LENSTR4 EQU $-STR4
	STR5 DB "CORRECT PASSWORD         "
	LENSTR5 EQU $-STR5
DATA ENDS

STK SEGMENT STACK
	DB 100 DUP(0)
	TOS LABEL WORD
STK ENDS

CODE SEGMENT
	ASSUME CS: CODE, DS:DATA, ES:DATA, SS:STK

WRITE PROC
		MOV AH, 40H
		MOV AL, 00H
		MOV BX, 0001H
		INT 21H
		RET
WRITE ENDP

READ PROC
		MOV AL, 00H
		MOV AH, 0AH
		INT 21H
		RET
READ ENDP
	
CHECK PROC
		LEA SI, STR3
		MOV AL, [SI+1]
		CMP AL, LENSTR2
		JNZ MISMATCH
		MOV CH,00H
		MOV CL, [SI+1]
		LEA DI, STR2
		INC SI
		INC SI
		CLD
	REPE CMPSB
		JNE MISMATCH
		
		LEA DX, STR5
		MOV CX, LENSTR5
		RET
MISMATCH:	
		LEA DX, STR4
		MOV CX, LENSTR4
		RET
CHECK ENDP
	
START:	MOV AX, STK
		MOV SS, AX
		MOV AX, DATA
		MOV DS, AX
		MOV ES, AX
		
		MOV CX, LENSTR1
		LEA DX, STR1
		CALL WRITE
		
		LEA SI, STR3
		MOV BYTE PTR [SI], LENSTR3-2
		LEA DX, STR3
		CALL READ
		
		CALL CHECK
		
		CALL WRITE
		
		MOV AL,00H
		MOV AH,4CH
		INT 21H
CODE ENDS
	END START