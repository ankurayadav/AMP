DATA SEGMENT 
	MY_LIST DB 40H,21H,69H,1AH,55H,65H
	ASIZE EQU $-MY_LIST
DATA ENDS

CODE SEGMENT
	ASSUME DS: DATA, CS: CODE
START:	MOV AX, DATA
		MOV DS, AX
		
		LEA BX, MY_LIST
		MOV CX, ASIZE
L:		MOV SI, 01H
	LI:	MOV AL, [BX+SI-1]
		MOV AH, [BX+SI]
		CMP AH, AL
		JC N
		MOV [BX+SI],AL
		MOV [BX+SI-1],AH
	N:	INC SI
		CMP SI, CX
		JNC L1
		JMP LI
	L1:	LOOP L
		
		MOV AH, 4CH
		INT 21H
CODE ENDS
	END START