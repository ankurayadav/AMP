DATA SEGMENT
	SELECT DB "SELECT YOUR CHOICE"
	SELECTLEN EQU $-SELECT
	SADD DB "1) ADD"
	SADDLEN EQU $-SADD
	SSUB DB "2) SUB"
	SSUBLEN EQU $-SSUB
	SMUL DB "3) MUL"
	SMULLEN EQU $-SMUL
	SDIV DB "4) DIV"
	SDIVLEN EQU $-SDIV
	ASK DB "ENTER TWO NUMBERS :"
	ASKLEN EQU $-ASK
	CHOICE DB 100 DUP(0)
	CLEN EQU $-CHOICE 
DATA ENDS

STK SEGMENT STACK
	DB 100 DUP(0)
	TOS LABEL WORD
STK ENDS

CODE SEGMENT 
	ASSUME CS:CODE, DS:DATA, SS:STK

PADD PROC NEAR
	ADD AL,BL
	RET
PADD ENDP

PSUB PROC NEAR
	SUB AL,BL
	RET
PSUB ENDP

PMUL PROC NEAR
	MUL BL
	RET
PMUL ENDP

PDIV PROC NEAR
	DIV BL
	RET
PDIV ENDP

WRITE PROC NEAR
	MOV CH, 00H
	MOV AH, 40H
	MOV BX, 0001H
	INT 21H
	RET
WRITE ENDP 

READ PROC READ
    LEA SI, CHOICE
	MOV BYTE PTR [SI], CLEN - 2
	
	MOV AH, 0AH
	LEA DX, CHOICE
	INT 21H
	RET
READ ENDP

POS MACRO ROW, COL
	MOV AH, 02H
	MOV DH, ROW
	MOV DL, COL
	MOV BH, 0
	INT 10H
POS ENDM

START:	MOV AX, DATA
		MOV DS, AX
		MOV AX, STK
		MOV SS, AX
		
		MOV CL, SELECTLEN
		LEA DX, SELECT
		CALL WRITE
		POS 1,0
		
		MOV CL, SADDLEN
		LEA DX, SADD
		CALL WRITE
		POS 2,0
		
		MOV CL, SSUBLEN
		LEA DX, SSUB
		CALL WRITE
		POS 3,0
		
		MOV CL, SMULLEN
		LEA DX, SMUL
		CALL WRITE
		POS 4,0
		
		MOV CL, SDIVLEN
		LEA DX, SDIV
		CALL WRITE
		POS 5,0 
		
		CALL READ
		POS 6,0
		
		MOV AL, 50H
		MOV BL, 04H
		
		LEA SI, CHOICE
		;MOV CL, [SI+1]
		;LEA DX, [SI+2]
		;CALL WRITE
		MOV CL, [SI+2]
		MOV CH, '1'
		CMP CL, CH
		JL EXIT
		JE ONE
		MOV CH, '4'
		CMP CL, CH
		JG EXIT
		JE FOUR
		
		MOV CH, '2'
		CMP CL, CH
		JE TWO
		
		MOV CH, '3'
		CMP CL, CH
		JE THREE
		JMP EXIT
		
		;MOV CL, ASKLEN
		;LEA DX, ASK
		;CALL WRITE
ONE:	CALL PADD
		DAA
		MOV AH, AL
		AND AH, 0F0H
		ROR AH, 4
		AND AL, 0FH
		MOV BL, AL
		MOV AL, AH
		
		mov ah, 2
		ADD AL, 30H
		mov dl, AL
		int 21h
		
		MOV AL, BL
		
		mov ah, 2
		ADD AL, 30H
		mov dl, AL
		int 21h
		
		JMP EXIT
TWO:	CALL PSUB
		DAS
		MOV AH, AL
		AND AH, 0F0H
		ROR AH, 4
		AND AL, 0FH
		MOV BL, AL
		MOV AL, AH
		
		mov ah, 2
		ADD AL, 30H
		mov dl, AL
		int 21h
		
		MOV AL, BL
		
		mov ah, 2
		ADD AL, 30H
		mov dl, AL
		int 21h
		JMP EXIT
THREE:	CALL PMUL
		JMP EXIT
FOUR:	MOV AH,00H
		CALL PDIV
		
EXIT:		
		MOV AH, 4CH
		INT 21H
CODE ENDS
	END START