DATA SEGMENT
	STR1 DB "Enter password :"
	LENSTR1 EQU $-STR1
	STR2 DB "ANKUR"
	LENSTR2 EQU $-STR2
	STR3 DB 100 DUP(0)
	LENSTR3 EQU $-STR3
	STR4 DB "WORNG PASSWORD           "
	LENSTR4 EQU $-STR4
	STR5 DB "CORRECT PASSWORD         "
	LENSTR5 EQU $-STR5
DATA ENDS

STK SEGMENT STACK
	DB 100 DUP(0)
	TOS LABEL WORD
STK ENDS

CODE SEGMENT
	ASSUME CS: CODE, DS:DATA, ES:DATA, SS:STK

POS MACRO ROW, COL
	MOV AH, 02H
	MOV DH, ROW
	MOV DL, COL
	MOV BH, 0
	INT 10H
POS ENDM
		
WRITE PROC
		MOV AH, 40H
		MOV AL, 00H
		MOV BX, 0001H
		INT 21H
		RET
WRITE ENDP

READ PROC
		MOV BL, 00H
		MOV AL, 00H
L:		MOV AH, 07H
		INT 21H
		CMP AL, 13
		JZ EXIT
		INC BL
		MOV [SI], AL
		INC SI
		JMP L
EXIT:
		RET
READ ENDP
	
CHECK PROC
		CMP BL, LENSTR2
		JNZ MISMATCH
		MOV CH,00H
		MOV CL, BL
		LEA SI, STR3
		LEA DI, STR2
		CLD
	REPE CMPSB
		JNE MISMATCH
		
		LEA DX, STR5
		MOV CX, LENSTR5
		RET
MISMATCH:	
		LEA DX, STR4
		MOV CX, LENSTR4
		RET
CHECK ENDP
	
START:	MOV AX, STK
		MOV SS, AX
		MOV AX, DATA
		MOV DS, AX
		MOV ES, AX
		
		MOV CX, LENSTR1
		LEA DX, STR1
		CALL WRITE
		
		LEA SI, STR3
		CALL READ
		POS 1,0
		CALL CHECK
		
		CALL WRITE
		
		MOV AL,00H
		MOV AH,4CH
		INT 21H
CODE ENDS
	END START