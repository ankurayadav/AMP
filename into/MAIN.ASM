DATA SEGMENT
	ERRMSG DB "OVERFLOW OCCURED"
	LENSTR EQU $-ERRMSG
DATA ENDS

STK SEGMENT STACK
	DW 50 DUP(0)
	TOS LABEL WORD
STK ENDS

PUBLIC ERRMSG
PUBLIC LENSTR
EXTRN OVERFLOW:FAR

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA, SS:STK

START:	MOV AX, DATA
	MOV DS, AX
	MOV AX, STK
	MOV SS, AX
	LEA SP, TOS
	
	MOV AX, 00
	MOV ES, AX
	
	MOV WORD PTR ES:[0010H], OFFSET OVERFLOW
	MOV WORD PTR ES:[0012H], SEG OVERFLOW
	
	MOV AX,7FFFH
	MOV BX,7FFFH
	ADD BX,AX
	INTO
	
	MOV AH, 4CH
	INT 21H
	
	CODE ENDS

END START